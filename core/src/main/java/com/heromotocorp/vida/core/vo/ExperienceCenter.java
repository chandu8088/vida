package com.heromotocorp.vida.core.vo;

public class ExperienceCenter {

	private String experienceCenterName;
	private String type;
	private String id;
	private String partnerId;
	private String isServiceCenter;
	private String latitude;
	private String longitude;
	private String address;
	private String postalCode;

	public String getExperienceCenterName() {
		return experienceCenterName;
	}

	public void setExperienceCenterName(String experienceCenterName) {
		this.experienceCenterName = experienceCenterName;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPartnerId() {
		return partnerId;
	}

	public void setPartnerId(String partnerId) {
		this.partnerId = partnerId;
	}

	public String getIsServiceCenter() {
		return isServiceCenter;
	}

	public void setIsServiceCenter(String isServiceCenter) {
		this.isServiceCenter = isServiceCenter;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getPostalCode() {
		return postalCode;
	}

	public void setPostalCode(String postalCode) {
		this.postalCode = postalCode;
	}

}
